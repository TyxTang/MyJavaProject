package Homework;

class TyxCircle {
    public static void main(String[] args) {
        double r = 3.0;
        double perimeter = 2 * Math.PI * r;
        double area = Math.PI * r * r;
        System.out.println("半径为3.0的圆的周长为：" + perimeter);
        System.out.println("半径为3.0的圆的面积为：" + area);
    }
}
